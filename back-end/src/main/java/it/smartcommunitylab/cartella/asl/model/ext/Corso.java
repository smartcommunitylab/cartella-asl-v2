package it.smartcommunitylab.cartella.asl.model.ext;

public class Corso {
	private String id;
	private String origin;
	private String extId;
	private String instituteId;
	private String schoolYear;
	private String dateFrom;
	private String dateTo;
	private String course;
	private String courseMetaInfoId;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getOrigin() {
		return origin;
	}

	public void setOrigin(String origin) {
		this.origin = origin;
	}

	public String getExtId() {
		return extId;
	}

	public void setExtId(String extId) {
		this.extId = extId;
	}

	public String getInstituteId() {
		return instituteId;
	}

	public void setInstituteId(String instituteId) {
		this.instituteId = instituteId;
	}

	public String getSchoolYear() {
		return schoolYear;
	}

	public void setSchoolYear(String schoolYear) {
		this.schoolYear = schoolYear;
	}

	public String getDateFrom() {
		return dateFrom;
	}

	public void setDateFrom(String dateFrom) {
		this.dateFrom = dateFrom;
	}

	public String getDateTo() {
		return dateTo;
	}

	public void setDateTo(String dateTo) {
		this.dateTo = dateTo;
	}

	public String getCourse() {
		return course;
	}

	public void setCourse(String course) {
		this.course = course;
	}

	public String getCourseMetaInfoId() {
		return courseMetaInfoId;
	}

	public void setCourseMetaInfoId(String courseMetaInfoId) {
		this.courseMetaInfoId = courseMetaInfoId;
	}

}
